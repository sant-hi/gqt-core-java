package com.gqt.corejava.intoductionChallenges;

public class AreaCircle {
	    public static void main(String[] args) {
	        double r = 5;
	        double pi = 3.14;

	        double area = pi * r * r;
	        double peri = 2 * pi * r;

	        System.out.println("Area = " + area);
	        System.out.println("Perimeter = " + peri);
	    }
	}


