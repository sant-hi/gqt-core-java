package com.gqt.corejava.DataTypeChallenges;
public class useofStringMethods {
	public static void main(String[] args) {
		String str = "Hello Java";
        System.out.println("Substring: " + str.substring(6));
        System.out.println("IndexOf 'J': " + str.indexOf('J'));
        System.out.println("Equals: " + str.equals("Hello Java"));
}
}
